def build_system_prompt(mode: AnalysisMode | None = None) -> str:
    """组装 system prompt：哲学基座 + 分析框架 + 模式约束 + 输出规范。"""
    effective = mode or AnalysisMode.AUTO
    if effective is AnalysisMode.AUTO:
        mode_section = _MODE_DECISION_TREE
    else:
        mode_section = MODE_GUIDES.get(effective, _MODE_DECISION_TREE)

    return "\n\n".join([SYSTEM_BASE, FRAMEWORK, mode_section, OUTPUT_SPEC])


def build_user_prompt(financial_context: str, mode: AnalysisMode | None = None) -> str:
    """组装 user prompt：交付财务数据并要求按规范输出。"""
    effective = mode or AnalysisMode.AUTO
    hint = (
        "请按上述决策树自行判断评分模式并分析。"
        if effective is AnalysisMode.AUTO
        else f"请按【{effective.value}】模式的评分维度分析。"
    )
    return f"""请分析以下公司的投资价值：

{financial_context}

{hint}给出完整的估值分析报告，严格遵循输出规范。"""
